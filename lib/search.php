function keytype($name)
{
    $index = 0;
    foreach ($GLOBALS['AUTOC'] as $key=>$value) {
        $index++;
        if (strpos(strtolower($key), $name) !== false) {
            break;
        }
    }    
    //echo "+++ index=".$index;
    // 1~10 cn brand
    // 11~33 en brand
    // 34~562 series & engine
    // 563~ parts
    if( $index<=10 )
        return "brand";
    if( $index>10 && $index<=33)
        return "ebrand";
    if( $index>34 && $index<=562)
        return "series";
    if( $index>563)
        return "module";
}	

